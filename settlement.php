                                You’ll want to bring proof of homeowners insurance, a copy of your contract with the seller, your home inspection reports, anything the bank required to approve your loan, and a government-issue photo ID. (Note to newlyweds who just changed their name: That ID needs to match the name that will appear on the property’s title and mortgage.)
                            </p>
                            <p class="collapse" id="settlement_more_info2" class="show-more">
                                <strong>Your Down Payment</strong><br>
                                You will already know from your disclosure form exactly how much you’ll have to cough up for a down payment and closing costs. Yet since a personal check won’t cut it, be sure to ask before closing whether you should wire transfer those funds or if you’ll need to bring a cashier’s check. Also bring your personal checkbook to closing, since that’s typically fine to pay smaller fees and may come in handy in case any unforeseen expenses crop up.
