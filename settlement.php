                            <strong>What to expect at closing</strong>
                        </h3>
                        <div>
                            <strong>A Bunch of People</strong>
                            <p>
                                Exactly who will be present at a closing (and where it’s held) depends on the state you live in, but there are certain supporting characters you can usually expect to make an appearance. The cast includes the home seller, the seller’s real estate agent as well as your own, a representative from a title company (more on that below), and, occasionally, a representative from the bank or lender where you got your loan.
                            </p>
                            <p class="collapse" id="settlement_more_info3" class="show-more">
                                <strong>Title Clearance</strong><br>
                                Before you can own or “take title” to a home, most lenders will require a title search of public property records to make sure there aren’t any liens or issues with transferring the property into your name (which is rare, but if something does crop up, it’s better to know that upfront).
                                <br><br>
                                <strong>Signing Your Name a Lot</strong><br>
                                You’ll be putting your John Hancock on a pile of legal documents (so be prepared for a mild hand cramp if you’re not used to writing in cursive).
                                <br><br>
                                <strong>A Few Curveballs</strong><br>
                                Be prepared for things to go awry at the closing, like someone gets stuck in traffic, a document is missing, or a name is misspelled. But don’t stress, simply do what’s in your power to make the day go off without a hitch. For instance, don’t schedule something two hours after the closing is supposed to start in case your closing runs over.

If all goes well (as it usually does), you will eventually leave your home closing with a stack of documents (which you should save) and the keys to your new home (finally!).
